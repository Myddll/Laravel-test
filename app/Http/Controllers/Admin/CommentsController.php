     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $comments = Comment::orderBy("created_at", "DESC")->paginate(10);
        return view("admin.comments.index", [
            "comments" => $comments,
        ]);
    }

